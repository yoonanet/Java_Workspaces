package exam03;

public class ComparisonOp { // 비교(관계)연산자 - 결과값이 true 아니면 false

	public static void main(String[] args) {
		//비교연산자는 총 6개 (왼쪽을 중심으로 비교)
		//비교연산자의 결과데이터를 저장하기 위한 자료형 -> boolean
		System.out.println(350 < 200);
		System.out.println(350 > 200);
		System.out.println(350 <= 200);
		System.out.println(350 >= 200);
		System.out.println(350 == 200);
		System.out.println(350 != 200);
	}

}
